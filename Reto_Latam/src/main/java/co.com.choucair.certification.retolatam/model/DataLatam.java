package co.com.choucair.certification.retolatam.model;

public class DataLatam {
    private String trayecto;
    private String origen;
    private String destino;
    private String idafecha;
    private String vueltafecha;


    public String getTrayecto() {
        return trayecto;
    }

    public void setTrayecto(String trayecto) {
        this.trayecto = trayecto;
    }

    public String getOrigen() {
        return origen;
    }

    public void setOrigen(String origen) {
        this.origen = origen;
    }

    public String getDestino() {
        return destino;
    }

    public void setDestino(String destino) {
        this.destino = destino;
    }

    public String getIdafecha() {
        return idafecha;
    }

    public void setIdafecha(String idafecha) {
        this.idafecha = idafecha;
    }

    public String getVueltafecha() {
        return vueltafecha;
    }

    public void setVueltafecha(String vueltafecha) {
        this.vueltafecha = vueltafecha;
    }
}

